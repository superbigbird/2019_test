public class 常量与变量3 {
    public static void main(String[] args) {
        //变量命名：满足驼峰命名规范
        int n1;
        n1=20;
        n1=30;

       // System.out.println("n1="+n1);

        //常量语法：final 数据类型 常量名=常量值;
        //常量的命名规则：常量名全部大写
        //特点“：常量一旦赋值后，后面就不能改变值
        final float PI=3.14F;
        final int HELLOWORLD = 200;
        final int A;
        A=20;
    }
}
